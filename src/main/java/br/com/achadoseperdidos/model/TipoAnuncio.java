package br.com.achadoseperdidos.model;

/**
 * Define se um anuncio se refere a um objeto perdido ou encontrado.
 */
public enum TipoAnuncio {
    /**
     * Anuncio criado por alguem que perdeu um objeto.
     */
    PERDIDO,

    /**
     * Anuncio criado por alguem que encontrou um objeto.
     */
    ENCONTRADO
}
